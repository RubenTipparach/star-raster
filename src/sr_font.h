/* Draw a single character at (x,y) into a framebuffer.
   color is packed RGBA (0xAABBGGRR). */
static inline void sr_draw_char(uint32_t *pixels, int fb_w, int fb_h,
                                int x, int y, char ch, uint32_t color)
{
    if (ch < SR_FONT_FIRST || ch > SR_FONT_LAST) return;
    const uint8_t *glyph = &sr_font_data[(ch - SR_FONT_FIRST) * SR_FONT_ROWS];
    for (int row = 0; row < SR_FONT_ROWS; row++) {
        int py = y + row;
        if (py < 0 || py >= fb_h) continue;
        uint8_t bits = glyph[row];
        for (int col = 0; col < SR_FONT_W; col++) {
            int px = x + col;
            if (px < 0 || px >= fb_w) continue;
            if (bits & (0x10 >> col))
                pixels[py * fb_w + px] = color;
        }
    }
}

/* Draw a null-terminated string. Returns the X position after the last char. */
static inline int sr_draw_text(uint32_t *pixels, int fb_w, int fb_h,
                               int x, int y, const char *str, uint32_t color)
{
    while (*str) {
        sr_draw_char(pixels, fb_w, fb_h, x, y, *str, color);
        x += SR_FONT_W + 1;  /* 1px spacing */
        str++;
    }
    return x;
}

/* Draw text with a 1px dark shadow for readability over any background. */
static inline void sr_draw_text_shadow(uint32_t *pixels, int fb_w, int fb_h,
                                       int x, int y, const char *str,
                                       uint32_t color, uint32_t shadow)
{
    sr_draw_text(pixels, fb_w, fb_h, x+1, y+1, str, shadow);
    sr_draw_text(pixels, fb_w, fb_h, x, y, str, color);
}

